/*
Write a program to input two numbers and display their sum.

Sample Test Cases:
Input 1:
3 4
Output 1:
Sum = 7

Input 2:
-1 20
Output 2:
Sum = 19

*/

#include <stdio.h>

int main()
{
    int a = 3, b = 4, c = -1, d = 20;

    printf("This is the sum %d\n", a + b);
    printf("This is the sum again %d\n", c + d);

    return 0;
}
